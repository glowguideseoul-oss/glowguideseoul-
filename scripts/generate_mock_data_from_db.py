def main() -> int:
    if not DB_PATH.exists():
        raise SystemExit(f"Missing DB: {DB_PATH}. Run scripts/init_clinic_db.py first.")

    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    clinics = fetch_clinics(conn)
    if not clinics:
        raise SystemExit("No published clinics found.")

    OUTPUT_PATH.write_text(render_file(clinics), encoding="utf-8")
    print(f"Wrote {OUTPUT_PATH}")
    print(f"Clinics exported: {len(clinics)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

