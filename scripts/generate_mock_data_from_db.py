#!/usr/bin/env python3
"""
Generate src/lib/mock-data.ts from the reviewed clinics table.

Usage:
  python3 scripts/generate_mock_data_from_db.py

This keeps the app static/demo-friendly while allowing clinic data to be
managed in SQLite first.
"""

from __future__ import annotations

import json
import sqlite3
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
DB_PATH = ROOT / "data" / "kclinic.db"
OUTPUT_PATH = ROOT / "src" / "lib" / "mock-data.ts"


def js_string(value: str | None) -> str:
    return json.dumps(value or "", ensure_ascii=False)


def js_array(values: list[str]) -> str:
    return json.dumps(values, ensure_ascii=False)


def fetch_clinics(conn: sqlite3.Connection) -> list[dict]:
    conn.row_factory = sqlite3.Row
    rows = conn.execute(
        """
        SELECT *
        FROM clinics
        WHERE profile_status = 'published'
        ORDER BY
          CASE sponsored_status WHEN 'sponsored' THEN 0 ELSE 1 END,
          display_name
        """
    ).fetchall()

    clinics = []
    for row in rows:
        clinic_id = row["id"]
        languages = [
            item["language_code"]
            for item in conn.execute(
                "SELECT language_code FROM clinic_languages WHERE clinic_id = ? ORDER BY language_code",
                (clinic_id,),
            ).fetchall()
        ]
        categories = [
            item["category"]
            for item in conn.execute(
                "SELECT category FROM clinic_categories WHERE clinic_id = ? ORDER BY category",
                (clinic_id,),
            ).fetchall()
        ]

        before_notes = row["before_visit_notes"] or ""
        before_checklist = [
            item.strip()
            for item in before_notes.replace("\n", ". ").split(".")
            if item.strip()
        ]
        if not before_checklist:
            before_checklist = [
                "Bring your passport",
                "Confirm appointment time before visiting",
                "Prepare allergy and medication information",
            ]

        clinics.append(
            {
                "id": str(row["id"]),
                "slug": row["slug"],
                "name": row["display_name"],
                "location": row["location_label"] or row["district"] or "Seoul",
                "categories": categories or ["Clinic Guide"],
                "languages": languages or ["EN"],
                "priceRange": row["price_range_label"] or "Price range listed after clinic confirmation",
                "sponsored": row["sponsored_status"] == "sponsored",
                "description": row["description"] or "Reviewed clinic guide for international medical travelers.",
                "foreignerSupport": row["foreigner_support"] or "Language support not yet verified",
                "beforeChecklist": before_checklist,
                "aftercareNotes": row["aftercare_notes"] or "Follow the clinic's aftercare instructions and contact the clinic if symptoms feel unusual.",
            }
        )

    return clinics


def render_clinics(clinics: list[dict]) -> str:
    blocks = []
    for clinic in clinics:
        blocks.append(
            "\n".join(
                [
                    "  {",
                    f"    id: {js_string(clinic['id'])},",
                    f"    slug: {js_string(clinic['slug'])},",
                    f"    name: {js_string(clinic['name'])},",
                    f"    location: {js_string(clinic['location'])},",
                    f"    categories: {js_array(clinic['categories'])},",
                    f"    languages: {js_array(clinic['languages'])},",
                    f"    priceRange: {js_string(clinic['priceRange'])},",
                    f"    sponsored: {str(clinic['sponsored']).lower()},",
                    f"    description: {js_string(clinic['description'])},",
                    f"    foreignerSupport: {js_string(clinic['foreignerSupport'])},",
                    f"    beforeChecklist: {js_array(clinic['beforeChecklist'])},",
                    f"    aftercareNotes: {js_string(clinic['aftercareNotes'])},",
                    "  },",
                ]
            )
        )
    return "\n".join(blocks)


